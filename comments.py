# We have a car with a gas tank size of 15 gallons
tank_size = 15

# The car can travel a distance of 600 miles on a full tank
distance = 600

# Calculate miles per gallon (mpg) by dividing the distance by the tank size
mpg = distance / tank_size

# Print out the result, showing how many miles the car can travel per gallon of gas
print("Miles per gallon:", mpg)
